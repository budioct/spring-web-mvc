package com.tutorial.mvc.pengenalan;

public class PengenalanMVC {

    /**
     * Sejarah MVC
     * ● MVC singkatan dari Model View Controller, yaitu salah satu software design pattern yang banyak
     *   digunakan ketika pengembangan aplikasi berbasis user interface
     * ● MVC pertama kali dikenalkan oleh Trygve Reenskaug pada tahun 1970 ketika berkunjung ke Xerox Palo Alto Research
     * ● Awalnya MVC banyak digunakan di aplikasi berbasis Desktop, namun sekarang MVC banyak diadopsi di Web
     * ● Saat ini sendiri, design pattern MVC sudah banyak berkembang, ada
     *   hierarchical-model–view–controller (HMVC),
     *   model–view–adapter (MVA),
     *   model–view–presenter (MVP),
     *   model–view–viewmodel (MVVM), dan lain-lain
     *
     *   Model View Controller
     * Seperti singkatannya, MVC dibagi menjadi tiga bagian :
     * ● Model, merupakan bagian yang merepresentasikan data. Seperti yang kita ketahui, ada banyak
     *   sekali jenis data, seperti data request, data response, data table, dan lain-lain, sehingga kadang kita
     *   perlu memperkecil lagi scope dari Model itu sendiri ketika membuat aplikasi.
     * ● View, merupakan bagian yang merepresentasikan tampilan, seperti halaman web, desktop, mobile,
     *   dan lain-lain.
     * ● Controller, merupakan bagian yang mengurus alur kerja dari menerima input, memanipulasi data
     *   Model, sampai menampilkan View. Anggap saja Controller merupakan core logic dari aplikasi kita
     *
     * Pada Kenyataannya
     * ● Walaupun sekilas MVC sangat sederhana, pada kenyataannya ketika kita membuat aplikasi yang
     *   kompleks, kita biasanya tidak lagi bisa memanfaatkan MVC
     * ● Kadang kita butuh mengimplementasikan design pattern lain, seperti misal nya Service Pattern,
     *   Repository Pattern, dan lain-lain
     * ● Oleh karena itu, jangan terlalu terpaku pada satu pattern, jika kita bisa mengkombinasikan
     *   beberapa pattern agar kode aplikasi kita lebih rapi dan baik, maka disarankan untuk melakukan kombinasi
     */

}
